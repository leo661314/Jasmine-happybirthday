# 要顯示的詞語
words = "HAPPY BIRTHDAY"

# 將每一個字母轉換為 "❤️" 形狀
output = ['' for _ in range(5)]
for char in words:
    for i in range(5):
        output[i] += letters[char][i] + '  '

# 打印輸出
for line in output:
    print(line.replace(' ', ' '))
